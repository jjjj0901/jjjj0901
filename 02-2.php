<p> 〈九成宮醴泉銘〉 　　　祕書監檢校侍中鉅鹿郡公臣魏方土徵奉勅撰</p>
<p>維貞觀六年孟夏之月，皇帝避暑乎九成之宮，此則隨之仁壽宮也。冠山抗殿，絕壑為池，跨水架楹，分巗竦闕。高閣周建，長廊四起。棟宇膠葛，臺榭參差。仰視則邁苕蔕百尋，下臨則崢嶸千仞。珠璧交暎，金碧相暉。照灼雲霞，蔽虧日月。觀其移山迴澗，窮泰極侈，以人從欲，良足深尤。至於炎景流金，無欎蒸之氣，微風徐動，有淒清之涼。信安體之佳所，誠養神之勝地。漢之甘泉，不能尚也。</p>
<p>皇帝爰在弱冠，經營四方，逮乎立年，撫臨億兆。始以武功壹海內，終以文德懷遠人。東越青丘，南踰丹儌，皆獻琛奉贄，重譯來王。西暨輪臺，北拒玄闕，並地列州縣，人充編戶。氣淑年和，邇安遠肅。群生咸遂，靈貺畢臻。雖藉二儀之功，終資一人之慮。遺身利物，櫛風沐雨，百姓為心，憂勞成疾。同堯肌之如腊，甚禹足之胼胝。針石屢加，腠理猶滯。爰居京室，每弊炎暑。群下請建離宮，庶可怡神養性。聖上愛一夫之力，惜十家之產。深閉固拒，未肯俯從。以為隨氏舊宮，營於曩代，棄之則可惜，毀之則重勞。事貴因循，何必改作？於是斵彫為樸，損之又損，去其泰甚，葺其頹壞。雜丹墀以沙礫，閒粉壁以塗泥。玉砌接於土階，茅茨續於瓊室。仰觀壯麗，可作鑒於既往，俯察卑儉，足垂訓於後昆。此所謂至人無為，大聖不作，彼竭其力，我享其功者也。</p>
<p>然昔之池沼，咸引谷澗。宮城之內，本乏水源。求而無之，在乎一物。既非人力所致，聖心懷之不忘。粵以四月甲申朔，旬有六日己亥，上及中宮，歷覽臺觀。閑步西城之陰，躊躇高閣之下。俯察厥土，微覺有潤。因而以杖導之，有泉隨而涌出。乃承以石檻，引為一渠。其清若鏡，味甘如醴。南注丹霄之右，東流度於雙闕。貫穿青瑣，縈帶紫房。激揚清波，滌蕩瑕穢，可以導養正性，可以澂瑩心神。鑒暎群形，潤生萬物。同湛恩之不竭，將玄澤之常流。匪唯乾象之精。蓋亦坤靈之寶。</p>
<p>謹案：《禮緯》云：「王者刑殺當罪，賞錫當功，得禮之宜，則醴泉出於闕庭。」《鶡冠子》曰：「聖人之德，上及太清，下及太寧，中及萬靈，則醴泉出。」《瑞應圖》曰：「王者純和，飲食不貢獻，則醴泉出，飲之令人壽。」《東觀漢記》曰：「光武中元元年，醴泉出京師，飲之者痼疾皆愈。」然則神物之來，寔扶明聖。既可蠲茲沈痼，又將延彼遐齡。是以百辟卿士，相趨動色。我后固懷撝挹，推而弗有。雖休勿休，不徒聞於往昔，以祥為懼，實取驗於當今。斯乃上帝玄符，天子令德，豈臣之末學，所能丕顯？但職在記言，屬茲書事。不可使國之盛美，有遺典策。敢陳實錄，爰勒斯銘。其詞曰：</p>
<p>惟皇撫運，奄壹寰宇。千載膺期，萬物斯覩。功高大舜，勤深伯禹。絕後光前，登三邁五。握機蹈矩，乃聖乃神。武克禍亂，文懷遠人。書契未紀，開闢不臣。冠冕並襲，琛贄咸陳。大道無名，上德不德。玄功潛運，幾深莫測。鑿井而飲，耕田而食。靡謝天功，安知帝力。上天之載，無臭無聲。萬類資始，品物流形。隨感變質，應德效靈。介焉如響，赫赫明明。雜遝景福，葳蕤繁祉。雲氏龍官，龜圖鳳紀。日含五色，烏呈三趾。頌不輟工，筆無停史。上善降祥，上智斯悅。流謙潤下，潺湲皎潔。蓱旨醴甘，冰凝鏡澈。用之日新，挹之無竭。道隨時泰，慶與泉流。我后夕惕，雖休弗休。居崇茅宇，樂不般遊。黃屋非貴，天下為憂。人玩其華，我取其實。還淳反本，代文以質。居高思墜，持滿戒溢。念茲在茲，永保貞吉。</p>
<p>兼太子率更令勃海男臣歐陽詢奉勅書</p>
