<p>〔正文〕<br />
  唯皇撫運（l），奄壹寰宇（2）。千載膺期（3），萬物斯覩（4）。功高大舜（5），勤深伯禹（6）。絕後光前（7），登三邁五（8）。握機蹈矩（9），乃聖乃神（10）。武克禍亂，文懷遠人（11）。書契未紀，開闢不臣（12）。冠冕並襲（13），琛贄咸陳（14）。大道無名（15），上德不德（16）。玄功潛運（17），幾深莫測（18）。鑿井而飲，耕田而食（19）。靡謝天功，安知帝力（20）。上天之載，無臭無聲（21）。萬類資始（22），品物流形（23）。隨感變質（24），應德效靈（25）。介焉如響（26），赫赫明明。雜遝景福（27），葳蕤繁祉（28）。雲氏龍宮（29），龜圖鳳紀（30）。日含五色（31），烏呈三趾（32）。頌不輟工（33），筆無停史（34）。上善降祥，上智斯悅（35）。流謙潤下（36），潺湲皎潔（37）。萍旨醴甘（38），冰凝鏡澈（39）。用之日新，挹之無竭（40）。道隨時泰（41），慶與泉流（42）。我后夕惕（43），雖休弗休。居崇茅宇，樂不般遊（44）。黃屋非貴（45），天下為憂。人玩其華，我取其實。還淳反本（46），代文以質。居高思墜，持滿戒溢（47）。念茲在茲（48），永保貞吉（49）。<br />
