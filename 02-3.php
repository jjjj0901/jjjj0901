<p>〔正文〕<br />
</p>
<p>謹案：《禮緯》云：「王者刑殺當罪，賞錫當功（1），得禮之宜（2），則醴泉出於闕庭。」《鶡冠子》曰：「聖人之德，上及太清（3），下及太寧（4），中及萬靈（5），則醴泉出。」《瑞應圖》曰：「王者純和（6），飲食不貢獻，則醴泉出，飲之令人壽。」《東觀漢記》曰：「光武中元元年（7），醴泉出京師，飲之者痼疾皆愈。」然則神物之來（8），寔扶明聖（9）。既可蠲茲沈痼（10），又將延彼遐齡。是以百辟卿士（11），相趨動色（12）。我后固懷撝挹，推而弗有（13）。雖休勿休，不徒聞於往昔，以祥為懼，實取驗於當今（14）。斯乃上帝玄符（15），天子令德，豈臣之末學，所能丕顯（16）？但職在記言，屬茲書事（17）。不可使國之盛美，有遺典策（18）。敢陳實錄，爰勒斯銘。其詞曰：<br />
</p>
<p>〔註釋〕</p>
<p>（1）《禮緯》：緯書的一種，對經書而言，漢人偽托為孔子所作。有《易緯》、《書緯》、《詩緯》、《禮緯》等七種，稱七緯。其書以儒家經義，附會人事吉兇禍福，預言治亂興廢，多有怪誕無稽之談。南朝宋時開始禁止緯書流傳。至隋，煬帝遣使搜焚其書。現所傳者為後人輯佚之作。錫：賜。<br />
  （2）禮：規定社會行為法則、規範、儀式的總稱。宣：得當。<br />
  （3）《鶡冠子》：相傳為先秦古籍之一，傳世有十九篇，作者姓名不詳，只知為楚人，「以鶡之冠，號曰鶡冠子」，屬雜家類。此處所引之文，見該書《鶡冠子．度萬．第八》。<br />
  太清：天。<br />
  （4）太寧：地名。<br />
  （5）萬靈：眾生靈、人類。<br />
  （6）《瑞應圖》：梁代孫柔之撰，共一卷。今傳有清代馬國翰所輯之《玉壺山房輯佚令》本，屬陰陽五行類。純和：純正平和，多指性格或氣質。<br />
  （7）《東觀漢記》：東漢劉珍等撰，是記載東漢歷史的重要史書。中元元年，即西元56年。<br />
  （8）神物：神奇靈異之物，此處指醴泉。<br />
  （9）寔：實、是。<br />
  （10）蠲：除去。沈痼：積久難治的病。<br />
  （11）百辟卿士：出自《詩．大雅．假樂》：「之綱之紀、燕及朋友。百辟卿士、媚于天子。不解于位、民之攸塈。」百辟，指眾諸侯。卿士，各級官員的泛稱，此處亦指公卿大官。<br />
  （12）相趨動色：彼此歡躍並且臉上已顯受感動的神色。<br />
  （13）後：君主。撝挹：謙遜。推而弗有；指「神物之來」，謙遜地並不認為是由於自己的「明聖」。<br />
  （14）以祥為俱，實取驗於當今：以祥瑞的出現感到畏懼，是有當今的先例以引為教訓。實指隋代出現的「祥瑞」，如：「小石變玉」、「河清」、「龍見」等，結果卻帶來災禍等情事。<br />
  （15）上帝：上天。玄符：天符：符命，謂上天顯示的瑞徽。符通符。<br />
  （16）丕：大。<br />
  （17）書事：記事。古代史官的職責，左史記言，古史記事。<br />
  （18）典策：典籍文獻。</p>
<p>〔翻譯〕</p>
<p>根據文獻記載，《禮緯》說：「帝王對犯人的判刑和處死確是量刑得當，對人的賞賜和該人的功勞相符，能做到符合社會規定的法則和規範，那醴泉對會在宮廷之間出現。」《鶡冠子》說：「聖人的恩德，能上達於天，下達於地，中達於眾生靈，那就會出現醴泉。」《瑞應圖》說：「帝王生性純正平和，飲食不用臣下貢獻的珍奇之物，那醴泉就會出現，喝了它能使人長壽。」《東觀漢記》說：「漢光武中元元年（西元56年），在京師（首都洛陽）出現了醴泉，喝了能使積久難治的病痊愈。」如此說來，神奇靈異的醴泉之所以出現，是為了扶助聖明的君王；它既可除去積久難治之病，又可使人延年益壽。對此，公卿大臣們無不歡躍並顯出受感動的神色，唯我皇上胸懷謙遜，並不認為醴泉的出現是由於自己的「明聖」。雖有美卻不以為美，不僅聽過去的聖賢如此，以祥瑞的出現感到畏懼，更是有當今的先例作為教訓的情事（隋代即有「祥瑞」出現卻帶來災禍的先例）。這是上天顯示的瑞徵，也說明天子的盛德，這些，豈是不學如臣這樣的人所能發揚光大！仁臣作為史官的職責必須「記言」、「記事」，不可使國事出現如此盛美之事而不見於史冊，故敢於如實陳述，寫成銘文刻碑。<a name="bottom"><a href="#top">  【返回頂部】</a></p>
</p>


