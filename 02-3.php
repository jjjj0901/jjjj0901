<p>〔正文〕</p>
<p>〈九成宮醴泉（1）銘〉    祕書監檢校侍中鉅鹿郡公，臣魏徵奉勅撰（2）</p>
<p>維貞觀六年孟夏之月（3），皇帝避暑乎九成之宮（4），此則隨之仁壽宮也（5）。冠山抗殿（6），絕壑為池（7），跨水架楹（8），分巗竦闕（9）。高閣周建，長廊四起。棟宇膠葛（10），臺榭參差。仰視則邁苕蔕百尋（11），下臨則崢嶸千仞（12）。珠璧交暎（13），金碧相暉。照灼雲霞，蔽虧日月。觀其移山迴澗，窮泰極侈（14），以人從欲（15），良足深尤（16）。至於炎景流金（17），無欎蒸之氣，微風徐動，有淒清之涼。信安體之佳所，誠養神之勝地。漢之甘泉，不能尚也（18）。<br />
</p>
<p>〔註釋〕<br />
  （1）醴泉：甘美的泉水。<br />
  （2）秘書監：掌管朝廷圖書典籍的長官，以少監為其副職。檢校侍中：兼領門下省侍中，此為加官。唐代中央政府分中書、門下、尚書三省，門下省負責審詔署奏之事，其長官稱「侍中」。「秘書監檢校侍中」，即秘書監兼任門下省侍中。勅：同「敕」，皇帝旨意。鉅鹿郡公：鉅鹿為封地，郡公為爵號，位在國公之下，縣公之上。<br />
  （3）維：語氣詞，無義。<br />
  （4）乎：於、在。<br />
  （5）隨：隋文帝楊堅受封於隨，公元581年滅北周，改隨為隋，以為國號。<br />
  （6）冠山抗殿：冠，覆蓋。抗，舉興。<br />
  （7）絕壑為池：絕，截堵。壑，山谷。池，既指地沼，也指宮外之護城河。<br />
  （8）楹：柱，實指橋柱。<br />
  （9）分巗聳闕：分，開辟。巗，同「巖」，險峻之地。竦闕，或言「聳闕」。闕，在宮門外築二臺，在臺上建樓觀，中央闕而為道，故謂之闕。<br />
  （10）棟宇膠葛：棟，屋之正中；宇，屋之四垂。棟宇，泛指房屋。膠葛：錯雜貌。<br />
  （11）苕蔕百尋：苕蔕，或作「迢遞」，高遠貌。尋，八尺為尋。<br />
  （12）崢嶸千仞：崢嶸，高峻也。仞，八尺為仞。<br />
  （13）交暎：或作「交映」。<br />
  （13）窮泰：泰，侈也。窮泰，過度奢侈。<br />
  （14）以：因。從：縱。<br />
  （15）尤：責備。<br />
  （16）炎景流金：炎景，暑天之酷熱。流，熔化。<br />
  （17）漢之甘泉，不能尚也：甘泉，漢之甘泉宮，在陜西淳化縣西北甘泉山，原為秦之離宮，漢武帝時增廣之，周長十九里，作為避暑行宮。尚，加，超過。</p>
<p>〔翻譯〕<br />
  貞觀六年夏曆四月，皇帝在九成宮避暑，這裏原是隋代的仁壽宮。覆蓋著山野而興建宮殿，截堵山谷以形成池沼和護城河。跨水立柱以架橋，開闢險峻之地建起聳立的雙闕，周圍建起高閣，四邊環繞長廊，房舍縱橫錯雜，臺榭參差交錯。仰望高遠可達百尋，俯看峻峭亦達千仞，輝煌如珠玉相映，金色和碧色交輝，其光彩能灼雲霞，其高峻能達日月。看他興建宮殿使山移潤迴，極盡奢侈之能事，因為人們的這種縱欲態度，實在應該痛加責備。至於當熱度可以溶化金屬的酷熱暑天，這裏卻無悶濕蒸熱的氣溫；微風徐徐吹來，帶來清涼的舒適，確是居住的好場所，實為調養精神的勝地，漢代的甘泉宮是不能超過它的。<a name="bottom"><a href="#top">  【返回頂部】</a></p>


<a name="q2">
<p>〔正文〕</p>
<p>皇帝爰在弱冠（1），經營四方（2），逮乎立年（3），撫臨億兆（4）。始以武功壹海內，終以文德懷遠人（5）。東越青丘（6），南踰丹儌（7），皆獻琛奉贄（8），重譯來王（9）。西暨輪臺（10），北拒玄闕（11），並地列州縣，人充編戶（12）。氣淑年和（13），邇安遠肅（14）。群生咸遂（15），靈貺畢臻（16）。雖藉二儀之功（17），終資一人之慮（18）。遺身利物（19），櫛風沐雨，百姓為心，憂勞成疾。同堯肌之如腊（20），甚禹足之胼胝（21）。針石屢加，腠理猶滯（22）。爰居京室（23），每弊炎暑。群下請建離宮（24），庶可怡神養性。聖上愛一夫之力，惜十家之產（25）。深閉固拒，未肯俯從。以為隨氏舊宮，營於曩代（26），棄之則可惜，毀之則重勞。事貴因循（27），何必改作？於是斵彫為樸（28），損之又損，去其泰甚，葺其頹壞。雜丹墀以沙礫，閒粉壁以塗泥。玉砌接於土階（29），茅茨續於瓊室（30）。仰觀壯麗，可作鑒於既往，俯察卑儉，足垂訓於後昆（31）。此所謂「至人無為，大聖不作」（32），彼竭其力，我享其功者也。<br />
</p>
<p>〔註釋〕<br />
  （1）爰在弱冠：爰，語首助詞，無義。弱冠，二十歲。按：唐太宗開始輔佐其父起兵爭天下，時年十八歲，這裏「弱冠」是概數。<br />
  （2）經營四方：出自《詩．小雅．北山》：「四牡彭彭，王事傍傍。嘉我未老，鮮我方將。旅力方剛，經營四方。」經營四方，此地指從事策劃和組織統一天下的活動。<br />
  （3）逮乎立年：逮乎，到了。立年，而立之年，三十歲。按：唐太宗稱帝時，年二十九歲，立年也為概數。<br />
  （4）撫臨億兆：撫臨，安撫並君臨。億兆：萬萬為億，萬億為兆，此處指天下億萬百姓。<br />
  （5）終以文德懷運人：終，引申為後來。文德，文明道德。懷遠人，懷念國家和民族。<br />
  （6）青丘：傳說之海外國名，泛指海外的蠻荒之地。<br />
  （7）丹僥：僥，邊界，古代稱南方的邊疆為丹僥。<br />
  （8）皆獻琛奉贄：奉獻珍寶以為晉見之禮。<br />
  （9）重譯來王：重譯，經過不同語言的輾轉翻譯。王，朝見。<br />
  （10）暨：抵，到。<br />
  （11）玄闕：古代傳說中北方極遠的地方。<br />
  （12）編戶：編入戶籍的平民。<br />
  （13）氣淑年和：氣淑，陰陽之氣溫和。年和，年景風調雨順。<br />
  （14）彌：近。<br />
  （15）群生：一切生物。遂：各得其所。<br />
  （16）靈貺：神靈降福。畢臻，都來。<br />
  （17）二儀：天地。<br />
  （18）一人：指皇帝，即唐太宗。<br />
  （19）遺：舍。<br />
  （20）堯：傳說中的遠古聖君。腊：同「臘」，乾肉，此指因風吹日曬使皮膚變得像乾肉一樣。<br />
  （21）禹：傳說中夏代的開國君主，治水有大功。胼胝：指皮膚的紋理和皮下肌肉之間的空隙。<br />
  （22）滯：停滯，此指血脈仍不通暢。<br />
  （23）京室：王寶，即京都。<br />
  （24）離宮：古人帝王於正式宮殿之外別建宮室，以便隨時遊處，謂之離宮。<br />
  （25）十家：即十戶。古代戶籍編制，五家為伍，十戶為什，相聯相保。此處十家指戶藉編制中的最小單位。<br />
  （26）曩代，過去的年代。<br />
  （27）因循：沿襲。<br />
  （28）雕：文飾。樸：質樸。<br />
  （29）玉砌：用玉砌成的階，用以形容豪華的石階。<br />
  （30）茅茨：茅草屋頂，也指茅屋。瓊室：用玉裝飾成的房子，用以形容豪華的房屋。<br />
  （31）後昆：後嗣子孫。<br />
  （32）至人無為，大聖不作：此二句引自《莊子．知北遊》：「聖人者，原天地之美而達萬物之理，是故至人無為，大聖不作，觀於天地之謂也。」</p>
<p>〔翻譯〕<br />
  皇帝約二十歲時，就從事策劃和組織統一天下的活動，到了約三十歲時，就做了億萬百姓的君主；一開始用武力統一中國，後來便以高度的文明道德使遠方的國家和民族親附：東邊越過青丘，南邊越過開僥，那些國家和民族都奉獻珍寶以為晉見之禮，經過不同語言的輾轉翻譯而來朝見；西到輪臺，北抵玄闕，都併入版圖而設置地方州縣，把那裏的人民編入戶籍以受朝廷管轄。陰陽之氣溫和，年景風調雨順，遠近都和平肅敬，一切生物也各得其所，神靈都來降福，這雖然是憑借天地的功德，但畢竟要依靠皇帝一個人的謀略。皇帝捨身以利天下黎民，在風雨飄搖之中，一心為百姓著想，憂國憂民積勞成疾，皮膚和堯帝一樣變成了乾肉的形態，手腳上結的肉繭更甚大禹，雖經針刺石砭治療，而血脈仍不通暢。住在京都，炎熱的暑天往往使人疲困不堪，群臣請求另建避暑行宮，庶幾可以療養從而心曠神怡。聖天子愛護每一個黎民的勞力，疼惜民間戶籍編制中最小單位的財貨，堅決拒絕，不肯聽從群臣的請求；提及隋代建築的舊宮殿是過去建造的，舍棄它感到可惜，毀掉它又會重新勞民傷財，應當沿襲既成的事實，又何必重新改作呢！於是去掉隋代舊宮的文飾而使之變得質樸，一再節儉，把原來過多奢華的部分去掉，把已經損壞的部分加以修正，使原來殿前紅色石階夾雜著沙礫，原來白色的墻壁夾雜著新塗的泥土；土階與原有的玉砌相接，茅屋連著原有的瓊室。仰看原有宮殿的壯麗，可吸取過去隋代由奢侈而敗亡的教訓；俯察今天修茸的求卑求儉，足以作為後嗣子孫的楷模，正提現了「至德之人表面上雖然沒有什麽作為，大聖也不妄自制作」的精神。隋代工匠竭盡全力大興土木建造的宮殿，卻使我安享了他們的成果。<a name="bottom"><a href="#top">  【返回頂部】</a></p>

