</p>
<p>〔註釋〕<br />
  （1）唯：語首助詞，無義。皇：皇上。撫運：順應時運。<br />
  （2）奄：同。奄壹：同一，統一。寰宇：天下。<br />
  （3）膺期：承受期運，即受天命為帝王。<br />
  （4）覩：同「睹」，引申為瞻仰。萬物：萬眾生靈。<br />
  （5）大舜：中國古代傳說中的聖君。<br />
  （6）伯禹：即夏禹王，治水功高，被後人尊為聖君。禹曾被封為伯爵，故又稱為伯禹。<br />
  （7）光：空。<br />
  （8）登：加，勝於。三：三皇，亦即「伏羲、神農、黃帝」，為傳說中的古代聖君。邁：超過。五：五帝，亦即「黃帝、帝顓頊、帝嚳、帝堯、帝舜」，為傳說中的古代聖君。<br />
  （9）握機：掌握天下的權柄。蹈矩：遵守禮法。<br />
  （10）乃：又。<br />
  （11）克：平定。懷：親附。遠人：遠方的國家和民族。<br />
  （12）開闢不臣：開天闢地以來不臣服者。<br />
  （13）冠冕：古代大夫以上之帽子，此處代指外國來朝之君臣。並襲。重重疊疊。<br />
  （14）琛：珍寶。贄：見面所贈之禮物。<br />
  （15）大道：自然法則，萬物必由之路。無名：不可言說。見《老子》第一章。<br />
  （16）「上德不德」：見《老子》第三十八章。<br />
  （17）立功：神功，調宇宙自然之力。潛運：默默地運行著。<br />
  （18）幾：神妙的跡象。<br />
  （19）「鑿井而飲，耕田而食」：相傳堯時，有老人擊壤而歌曰：「日出而作，日入而息，鑿井而飲，耕田而食，帝何力於我哉？」後成為歌頌盛世太平的典故。<br />
  （20）靡謝天功，安知帝力：靡：不，無。力：功。此二句即《擊壤歌》之「帝何力於我哉」之意。<br />
  （21）上天：天道。載：行，運行。無臭無聲：嗅無味聽無聲。<br />
  （22）萬類：萬物。資：賴以。始：生。<br />
  （23）品物：眾多的有生物和無生物。流：演變，變化。<br />
  （24）質：物類的本體。<br />
  （25）效：致。靈：福、善。<br />
  （26）介焉：通「芥」，細微貌。<br />
  （27）雜遝：同「雜沓」，眾多紛雜。景：大。<br />
  （28）葳蕤：紛披貌。祉：福。繁祉：多福。<br />
  （29）雲氏龍宮：「雲氏」，傳說黃帝受命時有雲瑞，故以雲紀事，即設置各部門長官都用雲字命名。「龍宮」，上古伏羲氏時，有龍瑞，故以龍命官，如春富為青龍之類。<br />
  （30）龜圖：即洛書。傳說堯時與群臣賢者到翠媯川，有大龜來投堯，龜背有圖，堯命臣下寫取以告瑞應。寫畢，魚還水中。鳳紀：據《左傳》記載，少皞即帝時，有鳳鳥飛來，故設置部門長官用鳥來命名。鳳鳥氏，就是歷正（管歷法的官）。後稱歲歷為「鳳歷」。上面所稱「雲氏」、「龍宮」、」龜圖」、「鳳紀」，都是祥瑞，後世以之為歌頌帝王瑞應之辭。<br />
  （31）日含五色：出自《易傳》：「聖王在上，則日光明而五色備」，以及《禮斗威儀》：「政理太平，則時日五色」。全句是說：以太陽呈現五色為「聖王在上」和「政理太平」的瑞應。<br />
  （32）烏呈三趾：傳說中太陽內的三足神鳥。《春秋元命苞》云：「日中有三足烏」，改太陽也稱「三足烏」、「金烏」、「靈鳥」。此外以太陽中的三足神烏出現說明瑞應。<br />
  （33）工：樂師，樂宮。<br />
  （34）史：史官，掌紀事。<br />
  （35）上善：最完美，主善。上智：智力特出。<br />
  （36）「流謙」：見《易．謙》：「地道變盈而流謙」，即流散盈滿以廣布於虛處。潤下：潤澤下土。<br />
  （37）皎：透明。<br />
  （38）旨：味美。萍：醴泉水上之浮萍。<br />
  （39）澈：澄清。<br />
  （40）挹：舀取。<br />
  （41）道：大道，即自然法則。隨：順暢。泰：通。<br />
  （42）與：同。<br />
  （43）我后：我君。夕惕：《易．乾》「君子終日乾乾，夕惕若厲，無咎。」意即「君子整天健強振作自己，直到夜間還警惕慎行」。<br />
  （44）般遊：般同「盤」，遊樂。<br />
  （45）黃屋：帝王車蓋，以黃繒為蓋裏，故名。此處代指帝王。<br />
  （46）反：同近，返回。<br />
  （47）戒：警惕。<br />
  （48）「念茲在茲」：語出《書．大禹謨》，後用以指念念不忘於某一事情。<br />
  （49）貞吉：純正美好。<br />
  （50）勅：同「敕」，帝王詔命。</p>
<p>〔翻譯〕</p>
<p>皇帝順應時運，統一天下，承受千載難逢的時運而為天子，為萬眾生靈所瞻仰；其功高於大舜，其勤勞勝於伯禹，真是空前絕後，超過古代聖君三皇五帝。皇帝掌握天下的權柄並遵守禮法，既聖德又神明，武能平定禍亂，文能使遠方的國家和民族親附。過去不見文字記載的、開天闢地以來不向中國臣服的都來親附，這些來朝的外國君臣冠冕重重，把作為見面禮的珍寶都陳列出來。大道不能用語言表述，上德之人雖然得「道」卻不以得為得，宇宙自然之力默默地運行著其神妙之跡象不可度量。上古《擊壤歌》唱著：「鑿井而飲，耕田而食。」天下太平，安居樂業，連上天之功都不感謝，又怎能知道皇帝對百姓之功！天道運行，人們嗅之無味聽之無聲，但萬物賴之以生，眾多的有生物和無生物因之以變化形態，物類的本體隨天造的感應而變化，天道能應人們的德行而致福，雖然德行細微天道也會響應，這是十分明白的。歷史上曾經出現的眾多紛雜巨大的福祉：如「雲氏」、「龍宮」、「龜圖」、「鳳紀」、太陽呈現五色、三足神烏從太陽中出現等等，樂官沒有停止過歌頌，史官沒有停止過用筆記載。今天因皇帝的至善而上天降臨吉祥，也因皇帝的智力特殊而給人世帶來喜悅，使醴泉從盈滿處流向虛處，從而潤澤下土，水聲潺潺，水質透明潔凈，水上的浮萍味美，水味如醴酒甘甜，水凝成體如明鏡般清澈，這樣的水日用日新，舀取它用之不盡。醴泉的出現說明「大道」應時而暢通無阻，吉慶同醴泉俱來，而我們的天子卻能日夜健強振作，警惕慎行，並不以吉慶為吉慶，居室崇尚茅屋，不以遊樂為樂，不以身為帝王為貴，而以天下的百姓為憂。人們都愛那花的美麗，我卻取其果實的實用，去浮華而歸於淳厚，棄枝葉而返回根本，以質樸代替文飾，居高處就要計慮防止下墜，端盛滿之水就要留心不外溢，要念念不忘這些，就能永遠保持純正美好。</p>
