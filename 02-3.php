<p>〔正文〕<br />
</p>
<p>然昔之池沼，咸引谷澗。宮城之內，本乏水源。求而無之，在乎一物（1）。既非人力所致，聖心懷之不忘（2）。粵（3）以四月甲申朔，旬有六日己亥（4），上及中宮，歷覽臺觀（5）。閑步西城之陰（6），躊躇高閣之下（7）。俯察厥土，微覺有潤。因而以杖導之（8），有泉隨而涌出。乃承以石檻，引為一渠。其清若鏡，味甘如醴。南注丹霄之右（9），東流度於雙闕（10）。貫穿青瑣（11），縈帶紫房（12）。激揚清波，滌蕩瑕穢，可以導養正性（13），可以澂瑩心神。鑒暎群形，潤生萬物。同湛恩之不竭（14），將玄澤之常流（15）。匪唯乾象之精（16）。蓋亦坤靈之寶（17）。<br />
</p>
<p>〔註釋〕<br />
  （1）本句第二字缺，或可補入「乎」字。<br />
  （2）聖心：尊稱皇帝之心。<br />
  （3）粵：語氣詞，無義。<br />
  （4）四月甲申朔，旬有六日己亥：按中國歷史干支紀日法，「朔」指是月初一，既知「朔」為甲申，則這個月的第十六日則是己亥。旬為十，有即又，「旬有六日」即十又六日。又，按歷表，唐貞觀年間只有「貞觀六年四月朔」為「甲申」，故文中指的年代為「貞觀六年（西元632年）」。<br />
  （5）臺觀：泛指樓臺亭榭。<br />
  （6）陰：指背面。<br />
  （7）躊躇：徘徊。<br />
  （8）導：掘開並引導。<br />
  （9）注：流灌。丹霄：宮殿名。<br />
  （10）雙闕：宮門外築二臺，臺上建樓觀。<br />
  （11）青瑣：宮門上鏤刻的圖紋，此代指宮門。<br />
  （12）紫房：皇太后居宮室。<br />
  （13）正性；純正的稟性。<br />
  （14）湛恩：湛，深。湛恩：深恩。<br />
  （15）玄澤：指天子的恩澤。<br />
  （16）乾象：天象。<br />
  （17）坤靈：地神。</p>
<p>〔翻譯〕<br />
  但是過去的池沼，水都從澗谷引來，宮城裏面，本來就缺乏水源，想求得水源結果又沒有，要解決這一問題，既不是人力所能辦到的。皇帝心裏對此一直念念不忘。貞觀六年四月十六日，皇帝來到中宮，沿途觀賞樓臺亭榭，信步走到西城的背面，在高聳的樓閣下徘徊，往下看到這裏的土地，略顯濕潤，於是用手杖掘地並加以導引，結果泉水隨之流湧出來，於是在泉水下邊砌上石檻。引來水流入石砌的溝渠。泉水清澈如鏡，水味甘甜如醴酒。泉水經過石渠，往南灌注在丹霄宮的右邊，往東流淌於雙闕之下；流泉貫穿於鏤刻圖紋的宮門，縈繞著太后所居的宮殿；泉水激揚起的清波，能將濁穢的渣滓蕩滌；它可以使人養成純正的稟性，可以使人的心神玲瓏剔透。泉水如鏡能照映出各種形態，由於它的滋潤可以使萬物生長，就如同皇帝的深恩永無休止，天子的恩澤永遠流布人問，它不僅是天象的精華，也是地神的環寶。
