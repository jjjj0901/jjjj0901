        <p>九成宮遺址 </p>
        <p>陜西寶雞 </p>
        <p> 九成宮遺址位於麟遊縣新城區,原為隋代仁壽宮,建於開皇十三年(593),由右仆射楊素監督,著名 建築學家宇文愷為檢校將作大匠,承擔設計和督促,開皇十五年三月建成。隋文帝六次到此避暑。唐貞觀 五年(631)唐太宗加以修繕和擴建,改名為九成宮。以後,大宗曾五次到此,每次住半年左右。高宗時, 改名為萬年宮,後又復名九成宮。&ldquo;九成&rdquo;者,謂&ldquo;九重&rdquo;或&ldquo;九層&rdquo;之意,言其高大。武則天以後,九成宮逐 漸荒蕪,唐未毀於洪水。1978年以來中國科學院考古研究所對遺址進行多次勘探、發掘。 1991年6月,發掘 出一座保存完好的唐代宮殿遺址,即九成宮第37號宮殿遺址。   九成宮內置禁苑、武庫及宮寺,周垣1800步,範圍包括今東北溝、梳妝臺、水漫嶺、天臺寺到西北 溝。東、西兩溝之間有天臺山,山上原有九龍殿,亦即排雲殿,有柱礎、石塊、殘磚、瓦及兩尊土闕的遺 跡。九龍殿東西兩面的山崖壁上可見當日建築的夯土層、石柱礎等痕跡。殿下平地,有一18平方米的夯築 土臺,名梳妝臺。西面有唐代福昌院,又名天山寺。寺北山坡上有貞觀六年所立的由魏征撰文、歐陽詢書 寫的&ldquo;九成宮禮泉銘&rdquo;碑和唐高宗撰&ldquo;萬年宮銘&rdquo;。   在九城宮遺址附近的官坪村,據載是唐宮百官府衙所在地,有馬坊、南坊、西坊、北坊,可能是屬禦 林軍宿營地和牧馬的地方。   遺址內發現唐代石砌水井。井臺呈長方形,刻有花紋圖案。還發現有石水渠、城門等遺跡。   37號遺址位於今縣城中心郵電局院內,坐北朝南,面闊五、進深二間,內部減柱,當心間稍寬,前後 有門,14個青石覆盆柱礎,排列有序,完整無缺。覆盆面上還留有木柱痕跡。南門西側、北門西側有兩塊 </p>
