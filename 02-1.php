        <p>《九成宮醴泉銘》,作者魏徵(580&mdash;643),字玄成,魏州曲城(今河北省巨鹿縣)人,青年時曾為道 士,後參加隋末李密領導的農民起義軍起義失敗後,投奔唐高祖李淵。唐太宗李世民繼位後,魏徵成為重 要輔臣,以直諫知名,官至左光祿大夫,封鄭國公,拜太子大師,卒溢文貞。他是唐代著名的政治家和史 學家。《醴泉銘》的書寫者歐陽詢(557&mdash;641),字信本,潭州臨湘(今湖南省長沙市)人。仕隋為大常 博士,唐太宗時官太子率更令(lvjingling)、弘文館學士,封渤海男。他是著名的書法家,尤工於楷書,《宣 和書譜》稱:&ldquo;詢工書為翰墨之冠&rdquo;,所書《醴泉銘》為其代表作之一。後人亦稱他為歐陽率更。 </p>
        <p>&ldquo;九成宮&rdquo;遺址,在今陜西麟遊縣城西2.5公?,原為隋之&ldquo;仁壽宮&rdquo;,唐貞觀五年(631)加以擴建,更名&ldquo;九 成宮&rdquo;,並置禁苑、武庫及宮寺。&ldquo;九成&rdquo;之意:&ldquo;成&rdquo;訓&ldquo;重&rdquo;,&ldquo;九&rdquo;訓&ldquo;多&rdquo;,&ldquo;九成&rdquo;形容多層,高峻。&ldquo;銘&rdquo;, 文體之一,多用韻語,如作山川、宮室、器物之銘前面多用散文敘述,然後是韻語銘文。《九成宮醴泉 銘》撰作和書寫於唐貞觀六年(632)夏歷四月,全文敘述了&ldquo;九成宮&rdquo;的來歷和其建築的雄偉壯觀,歌頌了 唐太宗的武功文治和節儉精神,介紹了宮城內發現醴泉的經過,並刊引典籍說明醴泉的出現是由於&ldquo;天子令 德&rdquo;所致,最後提出&ldquo;居高思墜,持滿戒盈&rdquo;的諫諍之言宋曾鞏在《九成宮酯泉銘&middot;跋》中稱:&ldquo;九成宮乃隋之 仁壽宮也,魏為此銘,亦欲太宗以隋為戒,可以見魏之誌也&rdquo; 。</p>
